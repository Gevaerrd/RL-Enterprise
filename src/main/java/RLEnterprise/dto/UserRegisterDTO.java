package RLEnterprise.dto;

import java.util.Objects;

import com.fasterxml.jackson.annotation.JsonProperty;

public class UserRegisterDTO {

    private Long id;
    private String name;
    private String email;
    private String password;
    @JsonProperty("g-recaptcha-response")
    private String recaptcha;

    public UserRegisterDTO() {

    }

    public UserRegisterDTO(String name, String email, String password) {
        this.id = id;
        this.email = email;
        this.password = password;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public void updateFirstName() {
        if (name != null && !name.isBlank()) {
            this.name = name.trim().split(" ")[0];
        } else {
            this.name = "";
        }
    }

    public String getFirstName() {
        if (name == null || name.isBlank()) {
            updateFirstName();
        }
        return name;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        return result;
    }

    @Override
    public String toString() {
        return "UserDTO [id=" + id + ", name=" + name + ", email=" + email + ", password=" + password + "]";
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final UserRegisterDTO other = (UserRegisterDTO) obj;
        if (!Objects.equals(this.name, other.name)) {
            return false;
        }
        if (!Objects.equals(this.email, other.email)) {
            return false;
        }
        return Objects.equals(this.password, other.password);
    }

    public String getRecaptcha() {
        return recaptcha;
    }

    public void setRecaptcha(String recaptcha) {
        this.recaptcha = recaptcha;
    }

}
